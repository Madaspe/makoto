defmodule Makoto.Accounts do
  @moduledoc """
  The Accounts context.
  """

  import Ecto.Query, warn: false
  alias Makoto.Repo

  alias Makoto.Accounts.{User, UserToken, UserNotifier}

  require Logger
  ## Database getters


  @doc """
  Gets a user by email.

  ## Examples

      iex> get_user_by_email("foo@example.com")
      %User{}

      iex> get_user_by_email("unknown@example.com")
      nil

  """
  def get_user_by_email(email) when is_binary(email) do
    Repo.get_by(User, email: email)
  end

  @doc """
  Gets a user by email and password.

  ## Examples

      iex> get_user_by_email_and_password("foo@example.com", "correct_password")
      %User{}

      iex> get_user_by_email_and_password("foo@example.com", "invalid_password")
      nil

  """
  def get_user_by_email_and_password(email, password)
      when is_binary(email) and is_binary(password) do
    user = Repo.get_by(User, email: email)
    if User.valid_password?(user, password), do: user
  end

  def get_user_by_username_and_password(username, password)
  when is_binary(username) and is_binary(password) do
  user = Repo.get_by(User, username: username)
  if User.valid_password?(user, password), do: user
end

  @doc """
  Updates a user.

  ## Examples

      iex> update_user(user, %{field: new_value})
      {:ok, %User{}}

      iex> update_user(user, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_user(%User{} = user, attrs) do
    user
    |> User.changeset(attrs)
    |> Repo.update()
  end

  ## User registration

  @doc """
  Registers a user.

  ## Examples

      iex> register_user(%{field: value})
      {:ok, %User{}}

      iex> register_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """

  def register_user(attrs, %{"promocode" => name_promocode} = params) do
    promocode =
      Makoto.Promocodes.get_promocode name_promocode
    case promocode do
      nil ->
        user =
          %User{}
          |> User.registration_changeset(attrs)
          |> Repo.insert()
        promocode ->
          Logger.info inspect(promocode)
          user_info =
            %User{}
            |> User.registration_changeset(Map.merge(attrs, %{"inviter_id" => promocode.owner_id}))
            |> Repo.insert()

          case user_info do
            {:ok, user} ->
              Makoto.Promocodes.apply_promocode(promocode, user)
              user_info
            _ ->
              user_info
          end
    end
  end

  def register_user(attrs, %{"inviter_id" => inviter_id} = params) do
    user =
      %User{}
      |> User.registration_changeset(Map.merge(attrs, %{"inviter_id" => inviter_id}))
      |> Repo.insert()
  end

  def register_user(attrs, _session) do
    %User{}
    |> User.registration_changeset(attrs)
    |> Repo.insert()
  end

